fun main() {
    //arithmetic operators
    var x = 90
    var y = 5
    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)
    println(x % y) //modulus

    //comparison operators
    var num1 = 45
    var num2 = 30
    println(num1 < num2)
    println(num1 > num2)
    println(num1 == num2) //equal to
    println(num1 != num2) //not equal to
    println(num1 <= num2)
    println(num1 <= num2)

    //assignment operators
    var number = 10
    println(number)

    number += 2
    println(number)

    number *= 2
    println(number)

    //logic operators - &&(and), ||(or), !(not)
    println( (100 > 20) && (80 < 10)) //and
    println( (100 > 20) || (80 < 10)) //or
    println( !(100 > 20) || (80 < 10)) //not



}